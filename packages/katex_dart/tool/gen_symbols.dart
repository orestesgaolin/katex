// ignore_for_file: avoid_print
//
// Generator for `lib/src/symbols/symbols.g.dart`.
//
// Approach
// --------
// KaTeX's `symbols.ts` is not a simple data file: it builds its `symbols`
// map imperatively via ~700 `defineSymbol(...)` calls *plus* several loops
// that bulk-register the Latin letters, digits, Greek capitals, and the
// Unicode "Mathematical Alphanumeric Symbols" range (bold / italic / fraktur
// / double-struck / script / sans / monospace) using surrogate-pair math.
// On top of that, `defineSymbol(..., acceptUnicodeChar)` registers a *second*
// entry keyed by the replacement character.
//
// Re-implementing all of that with a regex parser would be fragile and
// easy to get subtly wrong. Instead we run KaTeX's *own* registration logic
// and dump the resulting `symbols` map to JSON, then emit it verbatim as a
// Dart const table. This is maximally faithful: every entry KaTeX produces
// at runtime ends up in the generated table.
//
// Because `symbols.ts` only uses `import type` (type-only) imports, Node's
// `--experimental-strip-types` can run it directly once it is copied outside
// `node_modules` (Node refuses to strip types for files under node_modules).
//
// Run with:  dart run tool/gen_symbols.dart
//
// Requires: a `node` with `--experimental-strip-types` support (Node >= 22).

import 'dart:convert';
import 'dart:io';

const _katexSrc = '../../reference/node_modules/katex/src';

void main() {
  final scriptDir = File(Platform.script.toFilePath()).parent.path;
  final pkgRoot = Directory(scriptDir).parent.path; // packages/katex
  final symbolsTs = File('$pkgRoot/$_katexSrc/symbols.ts');
  if (!symbolsTs.existsSync()) {
    stderr.writeln('Cannot find KaTeX symbols.ts at ${symbolsTs.path}');
    exit(1);
  }

  // Stage the TS file + a tiny dumper outside node_modules so Node will
  // strip types and execute it.
  final tmp = Directory.systemTemp.createTempSync('katex_gen_symbols_');
  try {
    final stagedSymbols = File('${tmp.path}/symbols.ts')
      ..writeAsStringSync(symbolsTs.readAsStringSync());
    final dumper = File('${tmp.path}/dump.mts')
      ..writeAsStringSync('''
import symbols from "./symbols.ts";
const s = symbols as Record<string, Record<string, unknown>>;
process.stdout.write(JSON.stringify({math: s["math"], text: s["text"]}));
''');

    final result = Process.runSync(
      'node',
      ['--experimental-strip-types', dumper.path],
      stdoutEncoding: utf8,
      stderrEncoding: utf8,
    );
    if (result.exitCode != 0) {
      stderr
        ..writeln('node failed to dump KaTeX symbols (exit ${result.exitCode}):')
        ..writeln(result.stderr);
      stagedSymbols.deleteSync();
      exit(1);
    }

    final data = (jsonDecode(result.stdout as String) as Map)
        .cast<String, dynamic>();
    final math = (data['math'] as Map).cast<String, dynamic>();
    final text = (data['text'] as Map).cast<String, dynamic>();

    final buffer = StringBuffer()
      ..writeln('// GENERATED FILE — DO NOT EDIT.')
      ..writeln('//')
      ..writeln('// Generated by tool/gen_symbols.dart from KaTeX 0.17.0')
      ..writeln('// (reference/node_modules/katex/src/symbols.ts).')
      ..writeln('//')
      ..writeln('// ${math.length} math entries, ${text.length} text entries.')
      ..writeln('// ignore_for_file: type=lint, lines_longer_than_80_chars')
      ..writeln()
      ..writeln("import 'package:katex_dart/src/symbols/symbols.dart';")
      ..writeln()
      ..writeln('/// Symbol tables keyed by [Mode], mirroring KaTeX `symbols[mode][name]`.')
      ..writeln('const Map<Mode, Map<String, SymbolData>> symbolsTable = {')
      ..write(_emitMode('math', math))
      ..write(_emitMode('text', text))
      ..writeln('};');

    final outFile = File('$pkgRoot/lib/src/symbols/symbols.g.dart')
      ..createSync(recursive: true)
      ..writeAsStringSync(buffer.toString());

    print('Wrote ${outFile.path}');
    print('  math: ${math.length} entries, text: ${text.length} entries');
  } finally {
    tmp.deleteSync(recursive: true);
  }
}

String _emitMode(String mode, Map<String, dynamic> entries) {
  final buffer = StringBuffer()..writeln('  Mode.$mode: {');
  // Stable, deterministic ordering for clean diffs.
  final keys = entries.keys.toList()..sort();
  for (final name in keys) {
    final entry = (entries[name] as Map).cast<String, dynamic>();
    final font = entry['font'] as String;
    final group = entry['group'] as String;
    final replace = entry['replace'] as String?;
    buffer
      ..write('    ${_dartString(name)}: ')
      ..write('SymbolData(')
      ..write('Font.${_fontEnum(font)}, ')
      ..write('Group.${_groupEnum(group)}, ')
      ..write(replace == null ? 'null' : _dartString(replace))
      ..writeln('),');
  }
  buffer.writeln('  },');
  return buffer.toString();
}

String _fontEnum(String font) {
  switch (font) {
    case 'main':
      return 'main';
    case 'ams':
      return 'ams';
    default:
      throw StateError('Unknown font: $font');
  }
}

String _groupEnum(String group) {
  switch (group) {
    case 'bin':
      return 'bin';
    case 'close':
      return 'close';
    case 'inner':
      return 'inner';
    case 'open':
      return 'open';
    case 'punct':
      return 'punct';
    case 'rel':
      return 'rel';
    case 'accent-token':
      return 'accent';
    case 'mathord':
      return 'mathord';
    case 'op-token':
      return 'op';
    case 'spacing':
      return 'spacing';
    case 'textord':
      return 'textord';
    default:
      throw StateError('Unknown group: $group');
  }
}

/// Emits a Dart string literal. Non-ASCII characters are emitted as `\u{...}`
/// escapes so the generated file stays pure-ASCII and editor-safe (KaTeX does
/// the same for surrogate-pair wide characters).
String _dartString(String value) {
  final buffer = StringBuffer("'");
  for (final rune in value.runes) {
    switch (rune) {
      case 0x27: // '
        buffer.write(r"\'");
      case 0x5c: // \
        buffer.write(r'\\');
      case 0x24: // $
        buffer.write(r'\$');
      case 0x0a:
        buffer.write(r'\n');
      case 0x0d:
        buffer.write(r'\r');
      case 0x09:
        buffer.write(r'\t');
      default:
        if (rune < 0x20 || rune > 0x7e) {
          buffer.write('\\u{${rune.toRadixString(16)}}');
        } else {
          buffer.writeCharCode(rune);
        }
    }
  }
  buffer.write("'");
  return buffer.toString();
}
