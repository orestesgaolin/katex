// ignore_for_file: avoid_print
//
// Generator for `lib/src/symbols/spacing_data.g.dart`.
//
// Approach
// --------
// Ports KaTeX's `spacingData.ts` (the inter-atom spacing tables, in math
// units). The data is static, but to stay faithful we run KaTeX's own module
// via Node `--experimental-strip-types` and dump the `spacings` and
// `tightSpacings` exports to JSON, then emit them as Dart const maps keyed by
// [MathClass]. (Node refuses to strip types under node_modules, so the source
// is staged to a temp dir first.)
//
// Run with:  dart run tool/gen_spacing.dart
//
// Requires: a `node` with `--experimental-strip-types` support (Node >= 22).

import 'dart:convert';
import 'dart:io';

const _katexSrc = '../../reference/node_modules/katex/src';

void main() {
  final scriptDir = File(Platform.script.toFilePath()).parent.path;
  final pkgRoot = Directory(scriptDir).parent.path; // packages/katex
  final spacingTs = File('$pkgRoot/$_katexSrc/spacingData.ts');
  if (!spacingTs.existsSync()) {
    stderr.writeln('Cannot find KaTeX spacingData.ts at ${spacingTs.path}');
    exit(1);
  }

  final tmp = Directory.systemTemp.createTempSync('katex_gen_spacing_');
  try {
    File('${tmp.path}/spacingData.ts')
        .writeAsStringSync(spacingTs.readAsStringSync());
    final dumper = File('${tmp.path}/dump.mts')
      ..writeAsStringSync('''
import {spacings, tightSpacings} from "./spacingData.ts";
process.stdout.write(JSON.stringify({spacings, tightSpacings}));
''');

    final result = Process.runSync(
      'node',
      ['--experimental-strip-types', dumper.path],
      stdoutEncoding: utf8,
      stderrEncoding: utf8,
    );
    if (result.exitCode != 0) {
      stderr
        ..writeln('node failed to dump KaTeX spacing data '
            '(exit ${result.exitCode}):')
        ..writeln(result.stderr);
      exit(1);
    }

    final data = (jsonDecode(result.stdout as String) as Map)
        .cast<String, dynamic>();
    final spacings = (data['spacings'] as Map).cast<String, dynamic>();
    final tight = (data['tightSpacings'] as Map).cast<String, dynamic>();

    // Collect the distinct mu values so we can emit named constants that
    // mirror KaTeX's thinspace/mediumspace/thickspace.
    final named = {3.0: 'thinspace', 4.0: 'mediumspace', 5.0: 'thickspace'};

    final buffer = StringBuffer()
      ..writeln('// GENERATED FILE — DO NOT EDIT.')
      ..writeln('//')
      ..writeln('// Generated by tool/gen_spacing.dart from KaTeX 0.17.0')
      ..writeln('// (reference/node_modules/katex/src/spacingData.ts).')
      ..writeln('// ignore_for_file: type=lint')
      ..writeln()
      ..writeln("import 'package:katex_dart/src/symbols/symbols.dart';")
      ..writeln()
      ..writeln('/// 3 mu — thin space.')
      ..writeln('const Mu thinspace = Mu(3);')
      ..writeln()
      ..writeln('/// 4 mu — medium space.')
      ..writeln('const Mu mediumspace = Mu(4);')
      ..writeln()
      ..writeln('/// 5 mu — thick space.')
      ..writeln('const Mu thickspace = Mu(5);')
      ..writeln()
      ..writeln('/// Inter-atom spacings for display and text styles.')
      ..writeln('///')
      ..writeln('/// Mirrors KaTeX `spacings[left][right]`; a missing entry')
      ..writeln('/// means no spacing.')
      ..writeln('const Map<MathClass, Map<MathClass, Mu>> spacings = {')
      ..write(_emitTable(spacings, named))
      ..writeln('};')
      ..writeln()
      ..writeln('/// Inter-atom spacings for script and scriptscript styles.')
      ..writeln('///')
      ..writeln('/// Mirrors KaTeX `tightSpacings[left][right]`.')
      ..writeln('const Map<MathClass, Map<MathClass, Mu>> tightSpacings = {')
      ..write(_emitTable(tight, named))
      ..writeln('};');

    final outFile = File('$pkgRoot/lib/src/symbols/spacing_data.g.dart')
      ..createSync(recursive: true)
      ..writeAsStringSync(buffer.toString());

    print('Wrote ${outFile.path}');
  } finally {
    tmp.deleteSync(recursive: true);
  }
}

String _emitTable(Map<String, dynamic> table, Map<double, String> named) {
  const order = [
    'mord',
    'mop',
    'mbin',
    'mrel',
    'mopen',
    'mclose',
    'mpunct',
    'minner',
  ];
  final buffer = StringBuffer();
  for (final left in order) {
    if (!table.containsKey(left)) {
      continue;
    }
    final inner = (table[left] as Map).cast<String, dynamic>();
    if (inner.isEmpty) {
      buffer.writeln('  MathClass.$left: {},');
      continue;
    }
    buffer.writeln('  MathClass.$left: {');
    for (final right in order) {
      if (!inner.containsKey(right)) {
        continue;
      }
      final measurement = (inner[right] as Map).cast<String, dynamic>();
      final number = (measurement['number'] as num).toDouble();
      final unit = measurement['unit'] as String;
      if (unit != 'mu') {
        throw StateError('Unexpected spacing unit: $unit');
      }
      final value = named[number] ?? 'Mu($number)';
      buffer.writeln('    MathClass.$right: $value,');
    }
    buffer.writeln('  },');
  }
  return buffer.toString();
}
