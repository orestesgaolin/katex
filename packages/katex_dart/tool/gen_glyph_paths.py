#!/usr/bin/env python3
"""Generator: emits lib/src/svg/glyph_paths.g.dart — SVG path outlines for every
glyph in the embedded KaTeX fonts, keyed by font ('<Family>-<Variant>') and
Unicode codepoint.

The SVG serializer emits these as <path> elements instead of <text>, because
Chrome's SVG <text> rendering mangles some complex KaTeX glyphs (e.g. the Size2
integral renders as a degenerate triangle) even though the same font renders
correctly in HTML/Flutter. Path outlines are font-engine-independent and exact.

Outline coordinates are in font units (y-up). All KaTeX fonts are 1000 upm; the
serializer scales by fontSize/1000 and flips Y.

Requires fontTools:  pip install fonttools
Run from packages/katex root:  python3 tool/gen_glyph_paths.py
"""
import base64
import os
import sys

try:
    from fontTools.ttLib import TTFont
    from fontTools.pens.svgPathPen import SVGPathPen
except ImportError:
    sys.stderr.write("fontTools required: pip install fonttools\n")
    sys.exit(1)

# Same set as tool/gen_font_bytes.dart.
EMBED = [
    "KaTeX_Main-Regular", "KaTeX_Main-Bold", "KaTeX_Main-Italic",
    "KaTeX_Main-BoldItalic", "KaTeX_Math-Italic", "KaTeX_Math-BoldItalic",
    "KaTeX_AMS-Regular", "KaTeX_Caligraphic-Regular",
    "KaTeX_Size1-Regular", "KaTeX_Size2-Regular", "KaTeX_Size3-Regular",
    "KaTeX_Size4-Regular",
]

FONTS_DIR = "assets/fonts"
OUT = "lib/src/svg/glyph_paths.g.dart"


def main():
    if not os.path.isdir(FONTS_DIR):
        sys.stderr.write("assets/fonts not found; run from packages/katex root.\n")
        sys.exit(1)

    out = []
    out.append("// GENERATED by tool/gen_glyph_paths.py — do not edit by hand.")
    out.append("//")
    out.append("// SVG <path> outlines for every glyph in the embedded KaTeX fonts,")
    out.append("// keyed by font name ('<Family>-<Variant>') then Unicode codepoint.")
    out.append("// Coordinates are font units (y-up); the serializer scales by")
    out.append("// fontSize / katexGlyphUnitsPerEm and flips Y. See the generator header.")
    out.append("library;")
    out.append("")
    out.append("/// Units per em for all KaTeX fonts (the outlines' coordinate scale).")
    out.append("const double katexGlyphUnitsPerEm = 1000;")
    out.append("")
    out.append("/// font name -> codepoint -> SVG path `d` (font units, y-up).")
    out.append("const Map<String, Map<int, String>> katexGlyphPaths = {")

    total = 0
    for name in EMBED:
        path = os.path.join(FONTS_DIR, name + ".ttf")
        if not os.path.exists(path):
            sys.stderr.write("WARNING: missing %s; skipping.\n" % path)
            continue
        font = TTFont(path)
        upm = font["head"].unitsPerEm
        if upm != 1000:
            sys.stderr.write("WARNING: %s has unitsPerEm=%d (expected 1000)\n" % (name, upm))
        glyphset = font.getGlyphSet()
        cmap = font.getBestCmap()
        key = name.replace("KaTeX_", "")
        out.append("  '%s': {" % key)
        count = 0
        for codepoint in sorted(cmap.keys()):
            gname = cmap[codepoint]
            pen = SVGPathPen(glyphset)
            glyphset[gname].draw(pen)
            d = pen.getCommands()
            if not d:
                continue  # whitespace / empty glyph
            out.append("    0x%X: '%s'," % (codepoint, d))
            count += 1
            total += 1
        out.append("  },")
        sys.stderr.write("%s: %d glyphs\n" % (key, count))

    out.append("};")
    out.append("")

    with open(OUT, "w") as f:
        f.write("\n".join(out))
    sys.stderr.write("Wrote %s with %d glyph paths.\n" % (OUT, total))


if __name__ == "__main__":
    main()
