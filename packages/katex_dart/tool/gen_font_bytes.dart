// Generator: emits `lib/src/font/embedded_fonts.g.dart` containing base64-encoded
// TrueType bytes for the KaTeX fonts the MVP gallery needs. The SVG serializer
// (T-012) embeds these as `@font-face` data-URIs so output is self-contained.
//
// Run from the package root:
//   dart run tool/gen_font_bytes.dart
//
// Keep the embedded set scoped to the MVP families to avoid a multi-MB file.
// Add more families here as command coverage grows (T-019).
import 'dart:convert';
import 'dart:io';

/// Font files (without `.ttf`) to embed, keyed by the `<Family>-<Variant>`
/// name used by `KatexFont.fontName`. These cover the MVP gallery.
const _embed = <String>[
  'KaTeX_Main-Regular',
  'KaTeX_Main-Bold',
  'KaTeX_Main-Italic',
  'KaTeX_Main-BoldItalic',
  'KaTeX_Math-Italic',
  'KaTeX_Math-BoldItalic',
  'KaTeX_AMS-Regular',
  'KaTeX_Caligraphic-Regular',
  'KaTeX_Size1-Regular',
  'KaTeX_Size2-Regular',
  'KaTeX_Size3-Regular',
  'KaTeX_Size4-Regular',
];

void main() {
  final fontsDir = Directory('assets/fonts');
  if (!fontsDir.existsSync()) {
    stderr.writeln('assets/fonts not found; run from packages/katex root.');
    exit(1);
  }

  final buf = StringBuffer()
    ..writeln('// GENERATED by tool/gen_font_bytes.dart — do not edit by hand.')
    ..writeln('//')
    ..writeln('// Base64-encoded TrueType bytes for the MVP KaTeX fonts, keyed')
    ..writeln("// by the `<Family>-<Variant>` name (e.g. 'Main-Regular'). Used by")
    ..writeln('// the SVG serializer to embed `@font-face` data-URIs.')
    ..writeln('library;')
    ..writeln()
    ..writeln('const Map<String, String> katexFontBase64 = {');

  var count = 0;
  for (final name in _embed) {
    final file = File('assets/fonts/$name.ttf');
    if (!file.existsSync()) {
      stderr.writeln('WARNING: missing ${file.path}; skipping.');
      continue;
    }
    final b64 = base64.encode(file.readAsBytesSync());
    final key = name.replaceFirst('KaTeX_', '');
    buf.writeln("  '$key':");
    // Chunk the literal so lines stay manageable.
    const chunk = 100;
    for (var i = 0; i < b64.length; i += chunk) {
      final end = (i + chunk < b64.length) ? i + chunk : b64.length;
      final isLast = end == b64.length;
      buf.writeln("      '${b64.substring(i, end)}'${isLast ? ',' : ''}");
    }
    count++;
  }
  buf.writeln('};');

  File('lib/src/font/embedded_fonts.g.dart').writeAsStringSync(buf.toString());
  stdout.writeln('Wrote lib/src/font/embedded_fonts.g.dart with $count fonts.');
}
