// Generator: KaTeX `src/svgGeometry.ts` -> Dart `svg_geometry.g.dart`.
//
// Approach
// --------
// KaTeX's `svgGeometry.ts` has two parts:
//   1. A static `path` map: `Record<string, string>` of SVG `d` strings (a
//      mix of literal constants and a few wrapped in `doubleBrushStroke`).
//   2. Parameterized builders: `sqrtPath(size, extraVinculum, viewBoxHeight)`,
//      `tallDelim(label, midHeight)`, `innerPath(name, height)`, and
//      `phasePath(y)` — these depend on RUNTIME numbers, so they cannot be
//      pre-baked into a constant.
//
// This tool emits the static `path` map byte-faithfully by importing the
// upstream TS (after stripping the few TS-only annotations so Node can import
// it) and `JSON.stringify`-ing the map. The parameterized builders are
// hand-ported into the same generated file as Dart functions; this tool ALSO
// re-imports the TS builders and asserts the emitted builders match Node's
// output for a battery of sample inputs (so the hand-port stays faithful — a
// drift fails the generator).
//
// Run from the package root:
//   dart run tool/gen_svg_geometry.dart
//
// Requires `node` on PATH and the vendored KaTeX under `reference/`.
//
// The 1000:1 viewBox-to-em scale (KaTeX's convention) is carried through to
// the box tree / serializer / painter.
import 'dart:convert';
import 'dart:io';

void main() {
  final repoRoot = _findRepoRoot();
  final tsPath = '$repoRoot/reference/node_modules/katex/src/svgGeometry.ts';
  if (!File(tsPath).existsSync()) {
    stderr.writeln('svgGeometry.ts not found at $tsPath');
    exit(1);
  }

  const nodeScript = r'''
import {readFileSync, writeFileSync} from 'fs';
const tsPath = process.argv[2];
const src = readFileSync(tsPath, 'utf8');
let js = src
  .replace(/: Record<string,\s*string>/g, '')
  .replace(/\(([^)]*)\): string/g, '($1)')
  .replace(/: string/g, '')
  .replace(/: number/g, '')
  .replace(/export const/g, 'const');
js += '\nexport {path, sqrtPath, tallDelim, innerPath, phasePath};\n';
const tmp = tsPath + '.gen.mjs';
writeFileSync(tmp, js);
const m = await import('file://' + tmp);
const sqrtCases = [];
for (const size of ['sqrtMain','sqrtSize1','sqrtSize2','sqrtSize3','sqrtSize4']) {
  for (const ev of [0, 0.01, 0.025]) {
    sqrtCases.push([size, ev, 1080, m.sqrtPath(size, ev, 1080)]);
  }
}
for (const vbh of [1300, 2000, 3456]) {
  for (const ev of [0, 0.02]) {
    sqrtCases.push(['sqrtTall', ev, vbh, m.sqrtPath('sqrtTall', ev, vbh)]);
  }
}
const tallCases = [];
for (const label of ['lparen','rparen','lbrack','rbrack','vert','doublevert','lfloor','rfloor','lceil','rceil']) {
  for (const mh of [0, 1200, 1759]) {
    tallCases.push([label, mh, m.tallDelim(label, mh)]);
  }
}
const innerCases = [];
for (const name of ['⎜','∣','∥','⎟','⎢','⎥','⎪','⏐','‖']) {
  for (const h of [0, 1200]) {
    innerCases.push([name, h, m.innerPath(name, h)]);
  }
}
const phaseCases = [[800, m.phasePath(800)], [1234, m.phasePath(1234)]];
process.stdout.write(JSON.stringify({path: m.path, sqrtCases, tallCases, innerCases, phaseCases}));
''';

  final scriptFile = File('${Directory.systemTemp.path}/_gen_svg_geom.mjs')
    ..writeAsStringSync(nodeScript);
  final result = Process.runSync('node', [scriptFile.path, tsPath]);
  if (result.exitCode != 0) {
    stderr
      ..writeln('node failed:')
      ..writeln(result.stderr);
    exit(1);
  }
  final decoded = jsonDecode(result.stdout as String) as Map<String, dynamic>;
  final pathMap = (decoded['path'] as Map).cast<String, String>();

  // Verify the hand-ported Dart builders against Node's reference output for
  // every sampled input.
  for (final c in (decoded['sqrtCases'] as List).cast<List<dynamic>>()) {
    _verify(
      'sqrtPath(${c[0]},${c[1]},${c[2]})',
      c[3] as String,
      sqrtPath(c[0] as String, c[1] as num, c[2] as num),
    );
  }
  for (final c in (decoded['tallCases'] as List).cast<List<dynamic>>()) {
    _verify(
      'tallDelim(${c[0]},${c[1]})',
      c[2] as String,
      tallDelim(c[0] as String, c[1] as num),
    );
  }
  for (final c in (decoded['innerCases'] as List).cast<List<dynamic>>()) {
    _verify(
      'innerPath(${c[0]},${c[1]})',
      c[2] as String,
      innerPath(c[0] as String, c[1] as num),
    );
  }
  for (final c in (decoded['phaseCases'] as List).cast<List<dynamic>>()) {
    _verify('phasePath(${c[0]})', c[1] as String, phasePath(c[0] as num));
  }
  stdout.writeln('Verified hand-ported builders against Node: OK');

  final buf = StringBuffer()
    ..writeln('// GENERATED by tool/gen_svg_geometry.dart — do not edit by hand.')
    ..writeln('//')
    ..writeln('// Port of KaTeX `src/svgGeometry.ts`. The static [svgPath] map is')
    ..writeln('// byte-faithful to upstream; the parameterized builders')
    ..writeln('// ([sqrtPath], [tallDelim], [innerPath], [phasePath]) are ported')
    ..writeln('// and verified against Node over many sample inputs at generation')
    ..writeln('// time. In every path the viewBox-to-em scale is 1000:1.')
    ..writeln('//')
    ..writeln('// ignore_for_file: lines_longer_than_80_chars, unnecessary_library_directive')
    ..writeln('library;')
    ..writeln()
    ..writeln('/// Static SVG `d` strings keyed by KaTeX path name (stretchy')
    ..writeln("/// accents/arrows). Byte-faithful copy of `svgGeometry.ts`'s `path`.")
    ..writeln('const Map<String, String> svgPath = {');
  final keys = pathMap.keys.toList()..sort();
  for (final k in keys) {
    buf.writeln('  ${_dartStr(k)}: ${_dartStr(pathMap[k]!)},');
  }
  buf
    ..writeln('};')
    ..writeln()
    ..write(_buildersSource);

  final outPath = '$repoRoot/packages/katex/lib/src/svg/svg_geometry.g.dart';
  File(outPath).writeAsStringSync(buf.toString());
  stdout.writeln('Wrote $outPath (${pathMap.length} static paths).');
}

void _verify(String name, String expected, String actual) {
  if (expected != actual) {
    stderr
      ..writeln('MISMATCH for $name:')
      ..writeln('  node : ${jsonEncode(expected)}')
      ..writeln('  dart : ${jsonEncode(actual)}');
    exit(1);
  }
}

String _findRepoRoot() {
  var dir = Directory.current.absolute.path;
  while (true) {
    if (Directory('$dir/reference/node_modules').existsSync()) {
      return dir;
    }
    final parent = File(dir).parent.path;
    if (parent == dir) {
      stderr.writeln('Could not locate repo root (with reference/).');
      exit(1);
    }
    dir = parent;
  }
}

